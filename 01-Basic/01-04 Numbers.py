# 1. Initialize an integer
var1 = 1
print(var1)

# 2. Initialize a float
var2 = 1.00
print(var2)


# 3. Format number with 2 decimal places.
var3 = 3.1415
print("{0:.2f}".format(var3))
print(format(var3, '.2f'))


# 4. Compare an integer to a float
print(bool(1 == 1.00))  # True


# 5. Check if a number is between two numbers
print(1 < 5 < 10)  # True
